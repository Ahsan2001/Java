// System.out.println() and System.out.print() are two methods in Java used for printing output to the console. 
// The main difference between these two methods is the line ending:

// System.out.println(): 
// The println method adds a line break at the end of the output, so the next output will be printed on a new line.

// System.out.print(): 
// The print method does not add a line break at the end of the output, so the next output will be printed on the same line.
